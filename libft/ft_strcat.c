#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*ret;

	if (src == NULL || dest == NULL || !*src)
		return (dest);
	ret = dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		src++;
		dest++;
	}
	*dest = '\0';
	return (ret);
}
