#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int i;

	i = 0;
	if (n == 0)
		return (0);
	if (s1 == NULL)
		return (!(s2 == NULL));
	if (s2 == NULL)
		return (!(s1 == NULL));
	while (n > 0)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		else if (s1[i] == '\0')
			return (0);
		n--;
		i++;
	}
	return (0);
}
